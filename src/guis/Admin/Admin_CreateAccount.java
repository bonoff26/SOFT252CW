    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!passwordtxt.getText().equals("") && !passwordtxt.getText().equals("")) {
            if (passwordtxt.getText().equals(confirmpasstxt.getText())) {
                
                String type = typetxt.getText();
                if (type.equals("Patient")) {
                    Patient patient = new Patient();
                    patient.setName(nametxt.getText());
                    try {
                        patient.setPhoneNumber(Integer.parseInt(numbertxt.getText()));
                        patient.setEmail(emailtxt.getText());
                        patient.setAddress(addresstxt.getText());
                        patient.setPassword(passwordtxt.getText());
                        Data.patients = Data.createPatient(Data.patients, patient);
                        MessageBox.infoBox("Created Account", "Success");
                        this.dispose();
                        new AdminPage().setVisible(true);
                    }
                    catch (NumberFormatException e){
                        MessageBox.infoBox("Please enter a valid phone number", "Error");
                    }
                }
                else if (type.equals("Doctor")) {
                    Doctor doctor = new Doctor();
                    doctor.setName(nametxt.getText());
                    try {
                        doctor.setPhoneNumber(Integer.parseInt(numbertxt.getText()));
                        doctor.setEmail(emailtxt.getText());
                        doctor.setAddress(addresstxt.getText());
                        doctor.setPassword(passwordtxt.getText());
                        Data.doctors = Data.createDoctor(Data.doctors, doctor);
                        MessageBox.infoBox("Created Account", "Success");
                        this.dispose();
                        new AdminPage().setVisible(true);
                    }
                    catch (NumberFormatException e){
                        MessageBox.infoBox("Please enter a valid phone number", "Error");
                    }
                }
                else if (type.equals("Secretary")) {
                    Secretary secretary = new Secretary();
                    secretary.setName(nametxt.getText());
                    try {
                        secretary.setPhoneNumber(Integer.parseInt(numbertxt.getText()));
                        secretary.setEmail(emailtxt.getText());
                        secretary.setAddress(addresstxt.getText());
                        secretary.setPassword(passwordtxt.getText());
                        Data.secretaries = Data.createSecretary(Data.secretaries, secretary);
                        MessageBox.infoBox("Created Account", "Success");
                        this.dispose();
                        new AdminPage().setVisible(true);
                    }
                    catch (NumberFormatException e){
                        MessageBox.infoBox("Please enter a valid phone number", "Error");
                    }
                }
                else if (type.equals("Admin")) {
                    Admin admin = new Admin();
                    admin.setName(nametxt.getText());
                    try {
                        admin.setPhoneNumber(Integer.parseInt(numbertxt.getText()));
                        admin.setEmail(emailtxt.getText());
                        admin.setAddress(addresstxt.getText());
                        admin.setPassword(passwordtxt.getText());
                        Data.admins = Data.createAdmin(Data.admins, admin);
                        MessageBox.infoBox("Created Account", "Success");
                        this.dispose();
                        new AdminPage().setVisible(true);
                    }
                    catch (NumberFormatException e){
                        MessageBox.infoBox("Please enter a valid phone number", "Error");
                    }
                }
                


            }
            else {
                MessageBox.infoBox("Passwords do not match", "Error");
            }
        }
        else {
            MessageBox.infoBox("Please enter a password", "Error");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

