    private void findidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findidActionPerformed
        int out = 0;
        String type = typetxt.getText();
        String letter = "";
        switch (type) {
            case "Patient":
                out = 0;
                letter = "P";
                break;
            case "Doctor":
                out = 1;
                letter = "D";
                break;
            case "Secretary":
                out = 2;
                letter = "S";
                break;
            case "Admin":
                out = 3;
                letter = "A";
                break;
            default:
                break;
        }
        int id = out;
        out = Data.getNextID()[id];
        out++;
        String outString = "Next ID: " + letter + out;
        idtxt.setText(outString); 
    }//GEN-LAST:event_findidActionPerformed

