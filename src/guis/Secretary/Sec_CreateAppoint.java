    private void createbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createbtnActionPerformed
        Appointment appointment = new Appointment();
        int value = Data.findByID(doctxtbox.getText());
        int value2 = Data.findByID(patienttxtbox.getText());
        
        if (value != -1 && value2 != -1) {
            int pos = Data.findByID(doctxtbox.getText());
            Doctor doctor = Data.doctors[pos];
            appointment.setDoctor(doctor);
            appointment.setNotes(notestxt.getText());
            appointment.setTime(timetxt.getText());
            pos = value2;
            Patient patient = Data.patients[pos];
            Data.patients[pos].setDoctor(doctor);
            appointment.setPatient(Data.patients[Data.findByID(patienttxtbox.getText())]);

            patient.addAppointment(appointment);
            MessageBox.infoBox("Appointment request created! At time: " + timetxt.getText(), "Success");
            showData();
        }
        else {
            MessageBox.infoBox("No doctor with the ID: " + doctxtbox.getText() + " was found", "Error");
        }

    }//GEN-LAST:event_createbtnActionPerformed

