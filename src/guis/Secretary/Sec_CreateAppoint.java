    private void showData() {
                //show requests
        String display = "";
        boolean found = false;
        Patient[] patients = Data.patients;
        if (patients != null) {              
            for (int i = 0; i < patients.length; i++) {
                found = true;
                display = display + "Patient ID: " + patients[i].getId() + "\n" + "Name: " + patients[i].getName() + "\n\n";
                }
        }
        if (!found) {
            patientstxt.setText("No Patients found");
        }
        else{
            patientstxt.setText(display);
        }
        
        display = "";
        found = false;
        Doctor[] doctors = Data.doctors;
        if (doctors != null) {              
            for (int i = 0; i < doctors.length; i++) {
                found = true;
                display = display + "Doctor ID: " + doctors[i].getId() + "\n" + "Name: " + doctors[i].getName() + "\n\n";
                }
        }
        if (!found) {
            doctxt.setText("No Doctors found");
        }
        else{
            doctxt.setText(display);
        }
    }
